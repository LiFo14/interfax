Авторы нового видеообращения пообещали устроить всем участникам бомбардировок в Сирии повторение парижской трагедии
"Исламское государство" пригрозило терактами в Вашингтоне
Фото: Zuma/ТАСС

Москва. 16 ноября. INTERFAX.RU - Экстремисты из группировки "Исламское государство" (ИГ, запрещено в России), которая взяла на себя ответственность за гибель 129 человек в Париже, пригрозили терактами в Вашингтоне, сообщили в понедельник западные СМИ.

Они отмечают, что в очередном видео, распространенном в соцсетях исламистами из ИГ, боевики предупреждают страны, участвующие в нанесении авиаударов по территории Сирии, что их "постигнет участь Франции". При этом авторы видео особо акцентировали возможные атаки на Вашингтон.

"Мы говорим государствам, которые принимают участие в крестовом походе, что, клянемся богом, у вас наступит такой же день, как у Франции. Как мы поразили Францию в центре ее обители, Париже, также мы клянемся, что ударим по Америке в ее центре - Вашингтоне", - приводят СМИ слова одного из мужчин на видеозаписи.

Подлинность видео пока подтвердить не удалось, отмечают издания.

Вечером в пятницу, 13 ноября, исламисты устроили серию терактов в Париже. В результате, по последним данным, погибли не менее 129 человек. Ответственность за теракт по данным СМИ взяла на себя группировка ИГ.
